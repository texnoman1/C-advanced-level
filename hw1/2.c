/*
 Задача 2. Инверсия старших
Домашнее задание
На вход программе подается беззнаковое 32-битное целое число N. Требуется изменить значения всех
битов старшего байта числа на противоположные и вывести полученное таким образом число.
Данные на входе: Беззнаковое 32-битное целое число N
Данные на выходе: Одно целое число.
Пример №1
Данные на входе: 1
Данные на выходе: 4278190081
Пример №2
Данные на входе: 4278190081
Данные на выходе: 1
 */

#include <stdio.h>

// Функция инвертирования старшего байта числа n
unsigned int invert_high_byte(unsigned int n) {
    // Получаем старший байт числа
    unsigned int high_byte = (n >> 24) & 0xFF;

    // Инвертируем биты старшего байта
    unsigned int inverted_high_byte = ~high_byte;

    // Обновляем старший байт числа n с инвертированными битами
    unsigned int result = (n & 0x00FFFFFF) | (inverted_high_byte << 24);

    return result;
}

int main() {
    unsigned int n;
    printf("Введите беззнаковое 32-битное целое число: ");
    scanf("%u", &n);
    unsigned int result = invert_high_byte(n);
    printf("Результат: %u\n", result);
    return 0;
}
